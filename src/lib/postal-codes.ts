const REGION_PATH = Object.fromEntries(FLAT_REGIONS.map((r) => [r.code, r.path]))

export type PostalLevel = 'district' | 'city' | 'province'

export interface PostalResult {
  postal: string
  level: PostalLevel
}

export interface PostalLookupResult {
  postal: string
  regionCode: string
  regionPath: string
  level: PostalLevel
}

function provinceCodeOf(code: string): string {
  return `${code.slice(0, 2)}0000`
}

function cityCodeOf(code: string): string {
  return `${code.slice(0, 4)}00`
}

function resolvePostal(code: string): PostalResult | null {
  if (DISTRICT_POSTAL[code]) return { postal: DISTRICT_POSTAL[code], level: 'district' }
  if (CITY_POSTAL[code]) return { postal: CITY_POSTAL[code], level: 'city' }

  if (code.length === 6 && !code.endsWith('00')) {
    const cityCode = cityCodeOf(code)
    if (CITY_POSTAL[cityCode]) return { postal: CITY_POSTAL[cityCode], level: 'city' }
  }

  const provinceCode = provinceCodeOf(code)
  if (PROVINCE_POSTAL[provinceCode]) return { postal: PROVINCE_POSTAL[provinceCode], level: 'province' }
  return null
}

/** 根据区划代码查邮编，区县无精确数据时回退到地市 / 省级 */
export function getPostalCode(regionCode: string): PostalResult | null {
  if (!regionCode || regionCode.length < 2) return null
  return resolvePostal(regionCode.padEnd(6, '0').slice(0, 6))
}

export function postalLevelLabel(level: PostalLevel): string {
  switch (level) {
    case 'district': return '区县级'
    case 'city': return '市级'
    case 'province': return '省级参考'
  }
}

const POSTAL_INDEX: PostalLookupResult[] = (() => {
  const items: PostalLookupResult[] = []
  const add = (regionCode: string, postal: string, level: PostalLevel) => {
    items.push({
      postal,
      regionCode,
      regionPath: REGION_PATH[regionCode] ?? regionCode,
      level,
    })
  }
  for (const [code, postal] of Object.entries(PROVINCE_POSTAL)) add(code, postal, 'province')
  for (const [code, postal] of Object.entries(CITY_POSTAL)) add(code, postal, 'city')
  for (const [code, postal] of Object.entries(DISTRICT_POSTAL)) add(code, postal, 'district')
  return items
})()

/** 邮编反查地区，如 067000 → 承德市 */
export function lookupByPostalCode(input: string): PostalLookupResult[] {
  const postal = input.trim().replace(/\D/g, '')
  if (postal.length !== 6) return []
  return POSTAL_INDEX
    .filter((item) => item.postal === postal)
    .sort((a, b) => {
      const levelOrder = { district: 0, city: 1, province: 2 }
      return levelOrder[a.level] - levelOrder[b.level] || a.regionPath.localeCompare(b.regionPath, 'zh-CN')
    })
}
